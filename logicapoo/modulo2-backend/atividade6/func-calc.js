function calcNum(numero1, numero2, operacao) {
  if (operacao == 1) {
      return resultado = numero1 + numero2
  } else if (operacao == 2) {
      return resultado = numero1 - numero2
  } else if (operacao == 3) {
      return resultado = numero1 * numero2
  } else if (operacao == 4) {
      return resultado = numero1 / numero2
  } else {
      return 0
  }
};

console.log(calcNum(5, 5, 3))

